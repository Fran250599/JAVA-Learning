package ejemplos;

public class Conversiones {
    public static void main(String[] args) {

        double anguloEnGrados = 45;
        
        double anguloEnRadianes = Math.toRadians(anguloEnGrados);
        
        System.out.println("45 grados a radianes: " + anguloEnRadianes);
        
    }
    
}